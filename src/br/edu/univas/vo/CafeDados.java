package br.edu.univas.vo;

public class CafeDados {
	 private float medidas;
	 private int data;
	 private int medidasPagar;
	 private float valorMedida;
	 
	public float getMedidas() {
		return medidas;
	}
	public void setMedidas(float medidas) {
		this.medidas = medidas;
	}
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public int getMedidasPagar() {
		return medidasPagar;
	}
	public void setMedidasPagar(int medidasPagar) {
		this.medidasPagar = medidasPagar;
	}
	public float getValorMedida() {
		return valorMedida;
	}
	public void setValorMedida(float valorMedida) {
		this.valorMedida = valorMedida;
	}
}
